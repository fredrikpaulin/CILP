// C lowering — the deepest target. The host-language lowerings keep a relational runtime
// (terms threaded through a registry); C has none, so unification is compiled *fully* away
// into mode-directed function calls with no runtime term allocation. Each head predicate
// becomes `bool p(in-args…, out-ptrs…)` returning whether it holds: a body goal is a call
// whose in-args are values and out-args are written through pointers, a guard (a det test)
// is a bool in the conjunction, multiple clauses are tried in order, and recursion is a
// recursive call. The JSON interpreter is still the reference semantics.
//
// This forces requirements the other targets relax: complete modes (every position), and
// determinism — there is no backtracking runtime, so a nondeterministic primitive is
// reported non-lowerable. The mode/well-modedness analysis is shared (analyze.js); the
// determinism check and the C rendering are this file's. A lowered program compiles against
// copper.h (the term model) and a per-target C implementation of its primitives.

import { analyze } from "./analyze.js"

const cVar = name => `v_${name}`
const infeasible = reason => ({
  source: null,
  metadata: { target: "c", feasibility: "infeasible", caveats: [], reason, imports: [], entrypoints: [] }
})

// An in-position argument: a variable resolves to its C binding; a constant to a term
// literal (a C99 compound literal whose address is a term*).
const argExpr = a => (a.type === "var" ? cVar(a.name) : `&(term){ATOM, ${JSON.stringify(String(a.value))}, 0, 0}`)

// The parameter list for a predicate: in-args by value, then out-args by pointer.
function signature(headArgs, modes) {
  const params = []
  headArgs.forEach((_, i) => { if (modes[i] === "in") params.push(`term* _in${i}`) })
  let k = 0
  headArgs.forEach((_, i) => { if (modes[i] === "out") params.push(`term** out_${k++}`) })
  return params.join(", ")
}

function emitClause(clause, modes, indent) {
  const hm = modes.get(clause.head.predicate)
  const bound = new Set()
  const head = []
  clause.head.args.forEach((a, i) => {
    if (hm[i] === "in") { head.push(`${indent}  term *${cVar(a.name)} = _in${i};`); bound.add(a.name) }
  })

  const decls = []
  const conds = []
  let chk = 0
  for (const goal of clause.body) {
    const m = modes.get(goal.predicate)
    const callArgs = []
    const extra = []
    goal.args.forEach((a, i) => {
      if (m[i] === "in") {
        callArgs.push(argExpr(a))
      } else if (a.type === "var" && !bound.has(a.name)) {
        decls.push(cVar(a.name)); bound.add(a.name); callArgs.push(`&${cVar(a.name)}`)
      } else {
        // out position onto an already-bound variable or a constant: capture and compare.
        const tmp = `_chk${chk++}`
        decls.push(tmp); callArgs.push(`&${tmp}`); extra.push(`term_eq(${tmp}, ${argExpr(a)})`)
      }
    })
    conds.push(`${goal.predicate}(${callArgs.join(", ")})`, ...extra)
  }

  const lines = [...head]
  if (decls.length) lines.push(`${indent}  ${decls.map(d => `term *${d}`).join("; ")};`)
  let k = 0
  const outs = clause.head.args.filter((_, i) => hm[i] === "out").map(a => `*out_${k++} = ${cVar(a.name)};`)
  lines.push(`${indent}  if (${conds.length ? conds.join(" && ") : "true"}) { ${outs.join(" ")} return true; }`)
  return `${indent}{\n${lines.join("\n")}\n${indent}}`
}

export function lowerC(program, harness, options = {}) {
  const a = analyze(program, harness, options)
  if (!a.feasible) return infeasible(a.reason)
  const { caveats, modes, heads, groups } = a

  // C has no backtracking runtime, so every primitive a program calls must be deterministic.
  const determinism = new Map((harness?.primitives ?? []).map(p => [p.name, p.determinism]))
  const used = new Set(program.clauses.flatMap(c => c.body.map(g => g.predicate)))
  for (const name of used) {
    if (!heads.has(name) && determinism.get(name) === "nondet") {
      return infeasible(`${name}: C lowering requires deterministic predicates (no backtracking runtime)`)
    }
  }

  const runtime = options.runtime ?? "copper.h"
  const implementation = options.implementation ?? "copper_impl.h"

  const forward = groups.map(([pred, clauses]) => `bool ${pred}(${signature(clauses[0].head.args, modes.get(pred))});`)
  const defs = groups.map(([pred, clauses]) => {
    const body = clauses.map(c => emitClause(c, modes, "  ")).join("\n")
    return `bool ${pred}(${signature(clauses[0].head.args, modes.get(pred))}) {\n${body}\n  return false;\n}`
  })

  const source = [
    "// Generated by the copper-ilp C lowering. Do not edit by hand.",
    "// Reference semantics: the JSON interpreter. This code must match it.",
    `#include ${JSON.stringify(runtime)}`,
    `#include ${JSON.stringify(implementation)}`,
    "",
    ...forward,
    "",
    ...defs
  ].join("\n") + "\n"

  return {
    source,
    metadata: {
      target: "c",
      feasibility: caveats.length ? "caveats" : "ok",
      caveats,
      reason: null,
      imports: [runtime, implementation],
      entrypoints: groups.map(([p]) => p)
    }
  }
}
